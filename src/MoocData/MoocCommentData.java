package MoocData;

public class MoocCommentData {
	private String mComment;
	private String mCommentCourseid;
	private String mCommentUserid;
	private String mCommentUserName;
	private String mCommentPTime;
	
	public String getmComment() {
		return mComment;
	}
	public void setmComment(String mComment) {
		this.mComment = mComment;
	}
	public String getmCommentCourseid() {
		return mCommentCourseid;
	}
	public void setmCommentCourseid(String mCommentCourseid) {
		this.mCommentCourseid = mCommentCourseid;
	}
	public String getmCommentUserid() {
		return mCommentUserid;
	}
	public void setmCommentUserid(String mCommentUserid) {
		this.mCommentUserid = mCommentUserid;
	}
	public String getmCommentPTime() {
		return mCommentPTime;
	}
	public void setmCommentPTime(String mCommentPTime) {
		this.mCommentPTime = mCommentPTime;
	}
	public String getmCommentUserName() {
		return mCommentUserName;
	}
	public void setmCommentUserName(String mCommentUserName) {
		this.mCommentUserName = mCommentUserName;
	}
	
}
